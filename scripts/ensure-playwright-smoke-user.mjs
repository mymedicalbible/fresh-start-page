#!/usr/bin/env node
/**
 * Ensures a dedicated Auth user exists for Playwright smoke tests and writes `.env.playwright`.
 * Requires `SUPABASE_SERVICE_ROLE_KEY` in root `.env` (Dashboard → Settings → API → service_role).
 * The anon key (`VITE_SUPABASE_ANON_KEY`) cannot create users.
 *
 * Override defaults with PLAYWRIGHT_SMOKE_EMAIL / PLAYWRIGHT_SMOKE_PASSWORD in `.env` before running.
 */
import fs from 'node:fs'
import path from 'node:path'
import { fileURLToPath } from 'node:url'
import dotenv from 'dotenv'
import { createClient } from '@supabase/supabase-js'

const __dirname = path.dirname(fileURLToPath(import.meta.url))
const root = path.join(__dirname, '..')
dotenv.config({ path: path.join(root, '.env') })
dotenv.config({ path: path.join(root, '.env.local'), override: true })

const url = process.env.VITE_SUPABASE_URL?.trim()
const serviceRole = process.env.SUPABASE_SERVICE_ROLE_KEY?.trim()
const email =
  process.env.PLAYWRIGHT_SMOKE_EMAIL?.trim() || 'e2e-smoke@medical-bible.playwright'
const password =
  process.env.PLAYWRIGHT_SMOKE_PASSWORD?.trim() || 'MbE2eSmoke!LocalOnly2026'

if (!url) {
  console.error('ensure-playwright-smoke-user: missing VITE_SUPABASE_URL in .env')
  process.exit(1)
}

if (!serviceRole) {
  console.warn(
    [
      'ensure-playwright-smoke-user: SUPABASE_SERVICE_ROLE_KEY is not set in .env.',
      'Add it from Supabase Dashboard → Settings → API → service_role (secret).',
      'Then re-run: npm run test:e2e',
      'Or skip automation: put PLAYWRIGHT_SMOKE_EMAIL / PLAYWRIGHT_SMOKE_PASSWORD in .env.playwright manually.',
    ].join('\n'),
  )
  process.exit(0)
}

const admin = createClient(url, serviceRole, {
  auth: {
    autoRefreshToken: false,
    persistSession: false,
    detectSessionInUrl: false,
  },
})

function writePlaywrightEnv () {
  const playwrightEnvPath = path.join(root, '.env.playwright')
  let preserved = ''
  if (fs.existsSync(playwrightEnvPath)) {
    const prev = fs.readFileSync(playwrightEnvPath, 'utf8')
    const lines = prev.split('\n').filter((line) => {
      const t = line.trim()
      if (!t || t.startsWith('#')) return false
      const k = t.split('=')[0]?.trim()
      return k !== 'PLAYWRIGHT_SMOKE_EMAIL' && k !== 'PLAYWRIGHT_SMOKE_PASSWORD'
    })
    preserved = lines.join('\n')
  }
  const header = '# Auto-generated by scripts/ensure-playwright-smoke-user.mjs — gitignored; do not commit.'
  const body = [`PLAYWRIGHT_SMOKE_EMAIL=${email}`, `PLAYWRIGHT_SMOKE_PASSWORD=${password}`].join('\n')
  fs.writeFileSync(
    playwrightEnvPath,
    [header, body, preserved ? `\n${preserved}` : ''].join('\n').trim() + '\n',
    'utf8',
  )
  console.log('ensure-playwright-smoke-user: wrote', playwrightEnvPath)
}

async function main () {
  const { error: createErr } = await admin.auth.admin.createUser({
    email,
    password,
    email_confirm: true,
    user_metadata: { full_name: 'Playwright E2E' },
  })

  if (!createErr) {
    console.log('ensure-playwright-smoke-user: created user', email)
    writePlaywrightEnv()
    return
  }

  const msg = createErr.message ?? ''
  const duplicate = /already|registered|exists|duplicate/i.test(msg)
  if (!duplicate) {
    console.error('ensure-playwright-smoke-user: createUser failed:', createErr.message)
    process.exit(1)
  }

  const { data: listData, error: listErr } = await admin.auth.admin.listUsers({
    page: 1,
    perPage: 1000,
  })
  if (listErr) {
    console.error('ensure-playwright-smoke-user: listUsers failed:', listErr.message)
    process.exit(1)
  }

  const existing = listData.users.find((u) => u.email?.toLowerCase() === email.toLowerCase())
  if (!existing) {
    console.error('ensure-playwright-smoke-user: duplicate error but user not found:', email)
    process.exit(1)
  }

  const { error: updErr } = await admin.auth.admin.updateUserById(existing.id, {
    password,
    email_confirm: true,
  })
  if (updErr) {
    console.error('ensure-playwright-smoke-user: updateUser failed:', updErr.message)
    process.exit(1)
  }

  console.log('ensure-playwright-smoke-user: reset password for existing user', email)
  writePlaywrightEnv()
}

main().catch((e) => {
  console.error(e)
  process.exit(1)
})
